/**
 * 데이터 기간 문자열
 */
export function getDataRange() {
  const first = lottoData[0];
  const last = lottoData[lottoData.length - 1];
  return `${first.date.slice(0, 7)} ~ ${last.date.slice(0, 7)}`;
}
